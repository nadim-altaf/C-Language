#include <stdio.h>
int main()
{
    int n;
    printf("Enter no. of Rows :");
    scanf("%d", &n);
    for (int i = 1; i <= i; i++) // no. of rows -> denoted by i
    {
        for (int j = 1; j <= n; j++) // no. of columns -> denoted by j
        {
            printf("*");
        }
        printf("\n");
    }

    return 0;
}